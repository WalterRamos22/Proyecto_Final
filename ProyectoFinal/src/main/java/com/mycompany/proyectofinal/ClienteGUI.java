    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == ItemEvent.SELECTED) {
        // Obtener el valor seleccionado
        String selectedItem = (String) evt.getItem();
        if (selectedItem.equalsIgnoreCase("mysql")) {
            this.dbType = 2;
        } else {
            this.dbType = 1;
        }
        repositorioCliente = FactoryCliente.getFabricaCliente(dbType);
        repositorioProducto = FactoryProducto.getFabricaProducto(dbType);
        try {
            this.loadData();
        } catch (Exception ex) {
            System.out.println(ex);
        }
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

    public void loadData()  throws IOException, SQLException{
        repositorioCliente.cargarClientes(clienteTable);
        repositorioProducto.cargarProductos(tableProductos);
    }
    
